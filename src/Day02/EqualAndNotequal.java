package Day02;
//Program to equal operator and not equal operators
public class EqualAndNotequal {
    public static void main(String[] args)
    {
        int a=12,b=14;
        System.out.println(a==12);//a is equal to 12 true;
        System.out.println(a==b);//a is equal to b false;
        System.out.println(a!=b);//a is not equal to b that is true;
        System.out.println(a!=12);//a is not equal to 12 false;

    }
}
